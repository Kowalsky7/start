// assignment and decrement
#include <iostream>
using namespace std;
int main()
{
	int var1 = 10; // integer number 10
	cout << var1 << endl;
	var1 *= 2; // integer number 10 * 2 and output on the display
	cout << var1 << endl;
	var1 = --var1; // integer number 20 decrements and output on the display 
	cout << var1;
	return 0;
}